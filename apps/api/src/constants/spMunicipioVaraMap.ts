export function normalizeMunicipioKey(value: string): string {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim();
}

export function resolveSpMunicipioVara(municipio: string | null | undefined): SpMunicipioVaraItem | null {
  const key = normalizeMunicipioKey(municipio ?? "");
  if (!key) {
    return null;
  }

  return SP_MUNICIPIO_VARA_MAP[key] ?? null;
}
